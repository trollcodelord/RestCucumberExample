package serializeJSONintoList;

public class OpenResponse {
	
	public String cod;
	private int message;
	private int cnt;
	private List[] list;
	
	
	
	
	
	public String getCod() {
		return cod;
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public int getMessage() {
		return message;
	}
	public void setMessage(int message) {
		this.message = message;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public List[] getList() {
		return list;
	}
	public void setList(List[] list) {
		this.list = list;
	}
	
	
	
	
}
